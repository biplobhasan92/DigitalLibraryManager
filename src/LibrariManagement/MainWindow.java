    private void add_value_whenIssuBack(){
        int get_usr_id = Integer.parseInt(tf_sw_usrID.getText());
        int get_book_id = Integer.parseInt(tf_sw_bkid.getText());
        try {
            con = DriverManager.getConnection(connect + schema, user, DBpss);
            int giv_on = 0;
            pst = con.prepareStatement("update login_table set Book_issu_status = " + giv_on + " where User_ID = " + get_usr_id);
            pst.executeUpdate();

            int get_totl = 0;
            int get_avl = 0;
            int get_avlabl = 0;
            String sql2 = "select avilable_quantity, total_quantity from stor_book where book_id =" + get_book_id;
            pst = con.prepareStatement(sql2);
            rs = pst.executeQuery();
            while (rs.next()) {
                get_totl = (rs.getInt("total_quantity"));
                get_avl = (rs.getInt("avilable_quantity"));
                if(get_avl < get_totl){
                    get_avlabl = (rs.getInt("avilable_quantity")) + (1);
                
                }
            }

            if (get_avl > get_totl) {
                JOptionPane.showMessageDialog(rootPane, "Illigial Submition");
            } else if(get_avl <= get_totl) {
                
                String sqltats = "update stor_book set avilable_quantity=? where book_id = ?";
                pst = con.prepareStatement(sqltats);
                pst.setInt(1, get_avlabl);
                pst.setInt(2, get_book_id);
                pst.executeUpdate();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "test " + e.getMessage());
        }
        JOptionPane.showMessageDialog(rootPane, "Book Issue Back Successfully Complited !!");
    }
